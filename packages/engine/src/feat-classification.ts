/**
 * Full classification audit for issue #45's feat batch-extraction pass: every
 * one of the 390 feats the Foundry PF1 *system* pack shipped at the time of
 * the audit, bucketed and reasoned, mirroring the archetype-extracted/
 * pipeline's convention (see `archetype-extracted/index.ts`).
 *
 * `packages/data-pipeline/data/feats.json` now also carries ~3,150 feats
 * merged in from the community `pf1-content` module (see config.ts's
 * `PF_CONTENT_REPO`) — this audit was not redone against that much larger set
 * and doesn't claim to cover it. Extending it would mean individually reading
 * and cross-checking thousands more feats against the published rules, which
 * is out of scope for the data-source merge that added them; an unclassified
 * community feat simply isn't wired into FEAT_EFFECTS/FEAT_POOL_EFFECTS/
 * SITUATIONAL_FEAT_EFFECTS yet; it still displays (name, description,
 * prerequisites) — it just contributes no engine-derived number, same
 * end-state as this file's own "subsystem" bucket.
 *
 * Keyed by the feat's name slug (see `featNameSlug` in feat-effects.ts) rather
 * than RefEntity id, matching feat-effects.ts's own stability rationale: feat
 * ids in RefData are opaque Foundry UUIDs that may change between data
 * versions, but a name-derived slug is stable and human-authorable.
 *
 * Bucket definitions (adapted from the archetype rubric to feats):
 *  - "numeric"         — unconditional, always-on number. Extracted into
 *                        FEAT_EFFECTS (hand) or FEAT_EFFECTS_EXTRACTED (machine).
 *  - "choice-numeric"  — numeric but parameterized by a player choice (a
 *                        weapon type, a skill). Extracted ONLY when the
 *                        existing featChoices "skill"/"weapon" choice-UI shape
 *                        already covers it; otherwise classified and deferred.
 *  - "situational"     — a REAL number, but scoped to a specific maneuver,
 *                        weapon, enemy/ally state, or action the static sheet
 *                        can't detect without over-applying — the same honesty
 *                        bar traits.ts/feat-effects.ts's SITUATIONAL_FEAT_EFFECTS
 *                        already established. Never given an unconditional
 *                        Change.
 *  - "pool"            — raises an existing class-feature resource pool's max
 *                        (Extra Rage/Ki/... and friends). Wired through
 *                        FEAT_POOL_EFFECTS, consumed generically by
 *                        resources.ts's deriveResourcePools.
 *  - "subsystem"        — enables a mechanic, ability, proficiency, or
 *                        choice-list with no Change-shaped number to extract
 *                        at all.
 *  - "blocked"         — anything ambiguous, stacking-suspect (e.g. a
 *                        non-stacking AC type where RAW expects summing across
 *                        multiple takes), or requiring state/targets the
 *                        engine doesn't model (e.g. a per-school spell save DC
 *                        — no such target exists anywhere in targets.ts).
 *                        Downgrading to blocked (or situational) rather than
 *                        guessing a numeric entry is the point: wrong sheet
 *                        numbers are the worst failure mode.
 *
 * Methodology (disclosed, not hidden): every "numeric"/"choice-numeric"/"pool"/
 * "blocked" entry was individually read against the vendored feat description
 * (this repo's own OGL data slice) and cross-checked against the published PF1
 * rules (clean-room — no Foundry system code consulted, per CLAUDE.md). The
 * bulk "situational"/"subsystem" split for feats with no extraction candidate
 * was a heuristic-assisted pass (a regex scan for a "+N"/"-N" pattern in the
 * rules text, tag-driven for whole categories like Item Creation/Metamagic/
 * Critical/Combat Maneuver/Proficiency/Skill Unlock/Style/Teamwork) — this
 * mirrors archetype-extracted/fighter.ts's own disclosed pilot methodology.
 * The situational/subsystem boundary doesn't affect engine correctness
 * (neither bucket ever emits a Change), only audit-file clarity.
 */

export type FeatClassificationBucket =
  | "numeric"
  | "choice-numeric"
  | "situational"
  | "pool"
  | "subsystem"
  | "blocked";

